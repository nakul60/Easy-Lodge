        jTextField_fullname.setText(fullname);
        jTextField_phone.setText(phone);
        jTextField_email.setText(email);
        jTextArea_address.setText(address);
        
        Date bdate;
        try {
            bdate = new SimpleDateFormat("yyyy-MM-dd").parse(jTable_Tenants.getValueAt(index,2).toString());
            jDateChooser_birthdate.setDate(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(Form_Tenants.class.getName()).log(Level.SEVERE, null, ex);
        }
        
