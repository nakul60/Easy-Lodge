       public void populateJtableWithTenants()
    {
        //clear arraylist
        tenantsList.clear();
        //populate arraylist
        tenantsList = tenants.tenantsList();
        
        //jtable columns
        String[] columnsName = {"ID","Full Name","Birth Date","Phone","Email","Address"};
        
        //jtable rows
        Object[][] rows = new Object[tenantsList.size()][columnsName.length];
        
        for(int i=0;i<tenantsList.size();i++)
        {
            rows[i][0] = tenantsList.get(i).getId();
            rows[i][1] = tenantsList.get(i).getFullname();
            rows[i][2] = tenantsList.get(i).getBirthdate();
            rows[i][3] = tenantsList.get(i).getPhone();
            rows[i][4] = tenantsList.get(i).getEmail();
            rows[i][5] = tenantsList.get(i).getAddress();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        jTable_Tenants.setModel(model);
        
    }
    
       //create a function to display tenants
    public void displayTenants()
    {
        Classes.Tenants selected_tenants = tenantsList.get(index);
        jSpinner_id.setValue(selected_tenants.getId());
        jTextField_email.setText(selected_tenants.getEmail());
        jTextField_fullname.setText(selected_tenants.getFullname());
        jTextField_phone.setText(selected_tenants.getPhone());
        jTextArea_address.setText(selected_tenants.getAddress());
        
        Date bdate;
        try {
            bdate = new SimpleDateFormat("yyyy-MM-dd").parse(selected_tenants.getBirthdate());
            jDateChooser_birthdate.setDate(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(Form_Tenants.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
